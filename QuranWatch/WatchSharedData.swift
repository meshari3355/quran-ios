// MARK: - Helper functions

/// Get current azkar based on time rotation (every 2 hours)
func currentWatchAzkar() -> (text: String, category: String) {
    let cal = Calendar.current
    let dayOfYear = cal.ordinality(of: .day, in: .year, for: Date()) ?? 1
    let hour = cal.component(.hour, from: Date())
    let slot = hour / 2
    let idx = (dayOfYear * 12 + slot) % watchAzkarItems.count
    return watchAzkarItems[idx]
}

/// Get daily dua
func currentWatchDua() -> (text: String, occasion: String) {
    let dayOfYear = Calendar.current.ordinality(of: .day, in: .year, for: Date()) ?? 1
    let idx = (dayOfYear - 1) % watchDuaItems.count
    return watchDuaItems[idx]
}

/// Get daily verse
func currentWatchVerse() -> (text: String, ref: String) {
    let dayOfYear = Calendar.current.ordinality(of: .day, in: .year, for: Date()) ?? 1
    let idx = (dayOfYear - 1) % watchDailyVerses.count
    return watchDailyVerses[idx]
}

/// Compute Qibla angle from coordinates
func computeQiblaAngle(lat: Double, lon: Double) -> Double {
    let kaabaLat = 21.4225
    let kaabaLon = 39.8262
    let latR   = lat * .pi / 180
    let lonR   = lon * .pi / 180
    let kLatR  = kaabaLat * .pi / 180
    let kLonR  = kaabaLon * .pi / 180
    let dLon   = kLonR - lonR
    let y      = sin(dLon) * cos(kLatR)
    let x      = cos(latR) * sin(kLatR) - sin(latR) * cos(kLatR) * cos(dLon)
    var angle  = atan2(y, x) * 180 / .pi
    if angle < 0 { angle += 360 }
    return angle
}
