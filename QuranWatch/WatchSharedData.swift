import Foundation
import Combine

// MARK: - Prayer Time Model
struct WatchPrayerTime: Identifiable {
    let id = UUID()
    let name: String
    let time: String
    let icon: String

    var date: Date? {
        let fmt = DateFormatter()
        fmt.dateFormat = "HH:mm"
        guard let t = fmt.date(from: time) else { return nil }
        let cal = Calendar.current
        var comps = cal.dateComponents([.year, .month, .day], from: Date())
        let tComps = cal.dateComponents([.hour, .minute], from: t)
        comps.hour   = tComps.hour
        comps.minute = tComps.minute
        comps.second = 0
        return cal.date(from: comps)
    }
}

